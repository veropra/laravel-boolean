Chi siamo
Qui in Boolean trasformiamo ragazze/i con grandi potenzialità in Web Developer attraverso un percorso intensivo di 6 mesi di lezioni online aprendo loro nuove opportunità di carriera.
Abbiamo preso il meglio del mondo offline, ispirandoci ai benefici dell’insegnamento in aula (engagement, esercitazioni di gruppo nonchè quel senso di comunità che si sviluppa tra studenti) e l’abbiamo fuso con il mondo delle lezioni online creando un corso intensivo online di 6 mesi da seguire a tempo pieno senza doversi muovere da casa.
