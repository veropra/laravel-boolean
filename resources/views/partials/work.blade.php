Boolean Careers è una azienda giovane, nata da meno di un anno ma con grandi progetti per il futuro: abbiamo formato 40 studenti nel 2018 e triplicheremo il numero nel 2019: in Boolean si respira ambizione, positività ed un grande senso di sfida.

Cosa fa un Coding Coach
Il Coding Coach è il responsabile dell’insegnamento di una classe di studenti Boolean. Supportato da uno o più colleghi, dovrà coordinare e insegnare alla classe dal lunedì al venerdì, dalle 9.30 alle 13, seguendo il materiale didattico del corso.
Durante il pomeriggio sarà di supporto, dalle 14 alle 17, per risolvere eventuali dubbi degli studenti. Il tutto viene gestito con una serie di tool online, da Slack alla nostra Aula Virtuale, senza necessità di spostamenti.
Il lavoro non si ferma all’insegnamento dello stack menzionato sopra: dovrà contagiare gli studenti trasmettendo il DNA del developer dosando passione, know-how, caffeina e un’abbondante dose di ironia nerd.

